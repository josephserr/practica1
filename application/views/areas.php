            <div class="presentacion">
            <div class="row"> La Biblioteca Nacional del Ecuador guarda en sus estanterías casi todo lo que sobrevivió de una de las bibliotecas coloniales más importantes de toda América del Sur: la del Colegio Máximo de San Ignacio de Loyola de la Compañía de Jesús.

                Tras la expulsión de los jesuitas, los fondos bibliográficos del Colegio Máximo constituyeron la primera biblioteca pública del país, inaugurada el 25 de mayo de 1792, siendo Don Eugenio Espejo su primer bibliotecario, según el testimonio de William B. Stevenson, quien vino a Quito en 1808 como secretario particular del conde Ruiz de Castilla.

                La Biblioteca cambió su denominación por la Biblioteca Nacional, a inicios de la República y el primero en utilizarla fue el presidente Vicente Rocafuerte. En su informe a la nación de su gestión en 1838 decisión: “Se ha recompuesto y arreglado la Biblioteca Nacional” (Archivo del Congreso. Mensaje e Informes 1839).</div>
